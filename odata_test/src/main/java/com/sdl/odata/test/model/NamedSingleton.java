package com.sdl.odata.test.model;

import com.sdl.odata.api.edm.annotations.EdmEntity;
import com.sdl.odata.api.edm.annotations.EdmProperty;
import com.sdl.odata.api.edm.annotations.EdmSingleton;

import java.util.UUID;

/**
 * The Singleton Sample test model with Name.
 */
@EdmEntity(namespace = "ODataDemo", key = { "id" })
@EdmSingleton("SingletonWithName")
public class NamedSingleton {

    @EdmProperty(nullable = false)
    private UUID id;

    public UUID getId() {
        return id;
    }

    public void setId(UUID id) {
        this.id = id;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        NamedSingleton that = (NamedSingleton) o;

        if (id != null ? !id.equals(that.id) : that.id != null) {
            return false;
        }

        return true;
    }

    @Override
    public int hashCode() {
        return id != null ? id.hashCode() : 0;
    }

    @Override
    public String toString() {
        return "SingletonWithName{" +
                "id=" + id +
                '}';
    }
}
