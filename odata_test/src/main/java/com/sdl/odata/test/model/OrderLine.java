package com.sdl.odata.test.model;

import com.sdl.odata.api.edm.annotations.EdmEntity;
import com.sdl.odata.api.edm.annotations.EdmEntitySet;
import com.sdl.odata.api.edm.annotations.EdmNavigationProperty;
import com.sdl.odata.api.edm.annotations.EdmProperty;

/**
 * The Order Line test model.
 */
@EdmEntity(namespace = "ODataDemo", key = { "orderId", "lineId" })
@EdmEntitySet(name = "OrderLines", includedInServiceDocument = false)
public class OrderLine {

    @EdmProperty
    private long orderId;

    @EdmProperty
    private long lineId;

    @EdmNavigationProperty(nullable = false)
    private Product product;

    // NOTE: Wrapper types such as Integer should automatically map to Edm.Int32
    @EdmProperty
    private Integer quantity;

    public OrderLine() {
    }

    public long getOrderId() {
        return orderId;
    }

    public void setOrderId(long orderId) {
        this.orderId = orderId;
    }

    public long getLineId() {
        return lineId;
    }

    public void setLineId(long lineId) {
        this.lineId = lineId;
    }

    public Product getProduct() {
        return product;
    }

    public void setProduct(Product product) {
        this.product = product;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
